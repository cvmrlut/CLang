#include <stdio.h>
#include <stdlib.h>
int main()
{
	system("color f0");
    printf("用英文小写”w“ ”a“ ”s“ ”d“代表上下左右，终点END\n\n");
    char a[50][50]={"##############################	",
					"#C         #   ##   # ### ####	",
                    "# ###### # # #    # # ### ####	",
                    "# #   ## #   # #### # ###   ##	",
                    "#   # ##  ###    #  #  ## ####	",
                    "##### #     # ##### ##    ####	",
                    "#   # ##### #   #   # # #    #	",
                    "# # #    ## # #### ## # # ####	",
                    "# # # ##      ##      # # ####	",
                    "# # # ####### ## ###### #   ##	",
                    "# #   ##   # ## ###### ###   #	",
                    "# ###### # #####  #        # #	",
                    "#        # #     ##### ### #   END",
                    "# ######## ##### # ### ### # #	",
                    "#     # ## ##### ###       ###	",
                    "##### # ## #      ######## # #	",
                    "#     # ## ## ###        #   #	",
                    "# # ###       ###### ####### #	",
                    "# #    ### ##      #         #	",
                    "##############################	",
					};
    int i,x,y,p,q;
    char ch;
    x=1;
    y=1;
    p=12;
    q=29;
    for(i=0;i<20;i++)
    puts(a[i]);
    while(x!=p||y!=q)
    {
		ch=getch();
		if(ch=='s')
		{
			if(a[x+1][y]!='#')
			{
				a[x][y]=' ';
                x++;
                a[x][y]='C';
            }
        }
        if(ch=='w')
        {
			if(a[x-1][y]!='#')
            {
				a[x][y]=' ';
                x--;
                a[x][y]='C';
            }
        }
        if(ch=='a')
        {
			if(a[x][y-1]!='#')
            {
				a[x][y]=' ';
                y--;
                a[x][y]='C';
            }
        }
        if(ch=='d')
        {
			if(a[x][y+1]!='#')
            {
				a[x][y]=' ';
                y++;
                a[x][y]='C';
            }
        }
        system("cls");
        for(i=0;i<=20;i++)
        puts(a[i]);
    }
    system("cls");
    printf("\n恭喜过关！\n\n\n\n\a");
	system("pause");
	return 0;
}
